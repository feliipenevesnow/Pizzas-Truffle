    private void jLabel11MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel11MouseClicked
        if (UsuarioDAO.user.getSenha().equals(String.valueOf(senha.getPassword()))) {
            UsuarioDAO.user.setSenha(String.valueOf(novaSenha.getPassword()));
            UsuarioDAO control = new UsuarioDAO();

            control.update(UsuarioDAO.user);

            Notification notification = new Notification(null, true, "Sucesso", "Dado alterado.", false);
            notification.setVisible(true);
        } else {
            Notification notification = new Notification(null, true, "Atenção!", "Não foi possível realizar a ação.", false);
            notification.setVisible(true);
        }
    }//GEN-LAST:event_jLabel11MouseClicked

